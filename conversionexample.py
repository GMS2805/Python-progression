def cel2fah(c):
    """Converts from Celsius to Fahrenheit."""
    return 9/5 * c + 32

def fah2cel(f):
    """Converts from Fahrenheit to Celsius."""
    return 5/9 * (f - 32)

def km2mi(km):
    """Converts from kilometers to miles."""
    return km / 1.60934

def mi2km(mi):
    """Converts from miles to kilometers."""
    return mi * 1.60934